def get_matches(to_match,check_dict):
    matching = set()
    to_check_arr = cp.deepcopy(check_dict[to_match])

    for key,val in check_dict.items():
        if key == to_match:
            continue
        for _ in range(4):
            to_check_arr = list(zip(*reversed(to_check_arr)))
            to_check_arr = [list(x) for x in to_check_arr]
            for _ in range(4):
                val = list(zip(*reversed(val)))
                val = [list(x) for x in val]
                if to_check_arr[0] == val[len(check_dict[key]) -1] or to_check_arr[len(to_check_arr) -1] == val[0][::-1]:
                    matching.add(key)
    return matching

