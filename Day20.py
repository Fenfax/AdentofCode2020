def search_pic(full_map,search_pattern):
    map_save = cp.deepcopy(full_map)
    found_cnt = []
    max_search_x = max([x[0] for x in search_pattern])
    max_search_y = max([x[1] for x in search_pattern])
    for run_cnt in range(8):
        found_counter = 0
        for x in range(len(full_map)):
            for y in range(len(full_map[x])):
                if x+max_search_x < len(full_map) and y+max_search_y < len(full_map[x]):
                    found = True
                    for x_pos,y_pos in search_pattern:
                        if full_map[x+x_pos][y+y_pos] != "#":
                            found = False
                    if found:
                        for x_pos,y_pos in search_pattern:
                            full_map[x+x_pos][y+y_pos] = "O"
        
        for x in range(len(full_map)):
            for y in range(len(full_map[x])):
                if full_map[x][y] == "#":
                    found_counter += 1
        found_cnt.append(found_counter)
        if run_cnt == 4:
            tmp_map = []
            for x in map_save:
                tmp_map.append(x[::-1])
            full_map = cp.deepcopy(tmp_map)
        else:
            map_save = list(zip(*reversed(map_save)))
            map_save = [list(x) for x in map_save]
            full_map = cp.deepcopy(map_save)
    return found_cnt
        

