def get_data(inp):
    using = ["e", "se", "sw", "w", "nw", "ne"]
    out = []
    for y in inp:
        tile = []
        while len(y) > 0:
            for x in using:
                if y.startswith(x):
                    tile.append(x)
                    y = y[len(x):]
        out.append(tile)

    real_out = []
    for x in out:
        x_pos = 0
        y_pos = 0
        for y in x:
            if y == "w":
                x_pos -= 2
            elif y == "nw":
                x_pos -= 1
                y_pos += 1
            elif y == "ne":
                x_pos += 1
                y_pos += 1
            elif y == "e":
                x_pos += 2
            elif y == "se":
                y_pos -= 1
                x_pos += 1 
            elif y == "sw":
                y_pos -= 1
                x_pos -= 1

        real_out.append((x_pos,y_pos))        

    return real_out

def check_blacks(color_dict,pos):
    to_check = [(-2,0),(-1,1),(1,1),(2,0),(1,-1),(-1,-1)]
    blacks = 0
    for x in to_check:
        check_pos = tuple(map(sum,zip(x,pos)))
        if color_dict.get(check_pos) == "B":
            blacks += 1
    return blacks

